                <p>Pura Lingsar adalah salah satu situs suci yang paling unik dan bersejarah di Pulau Lombok, Provinsi Nusa Tenggara Barat, Indonesia. Dibangun pada tahun 1714 Masehi, pura ini adalah perwujudan harmoni antara dua agama besar di Indonesia, yaitu Hindu dan Islam, serta mencerminkan toleransi agama yang tinggi di wilayah ini.</p>
        
                <p>Pura Lingsar adalah tempat ibadah utama bagi penganut Hindu Sasak di Lombok. Namun, yang membuat pura ini istimewa adalah penggunaannya juga oleh komunitas Muslim Wetu Telu, yang merupakan perpaduan unik dari Islam dan tradisi animisme Sasak. Hal ini menjadikan Pura Lingsar sebagai simbol kerukunan antaragama yang kuat dan toleransi yang berlangsung selama berabad-abad di pulau ini. </p>
                
                <p>Arsitektur Pura Lingsar mencerminkan campuran gaya arsitektur Hindu dan Islam. Kompleks pura terdiri dari beberapa bangunan, termasuk Meru, tempat suci bagi Dewa-Hindu, dan tempat ibadah muslim yang disebut Bale Kulkul. Di tengah kompleks, terdapat kolam suci yang disebut Segara Anak. Di sekitarnya, terdapat persembahan-persembahan dan altar-alter yang digunakan dalam upacara keagamaan.</p>
                 <img src="gambar2.jpg" class="gambar2">
                <p>Pura Lingsar menjadi pusat perayaan berbagai upacara keagamaan bagi kedua komunitas, baik Hindu maupun Muslim. Salah satu upacara yang terkenal adalah Perang Topat, di mana umat Hindu dan Muslim saling berbagi ketupat (nasi yang dibungkus dalam janur) dalam bentuk saling melempar, sebagai ungkapan rasa syukur atas hasil panen dan sebagai simbol persatuan.</p>
                
                <p>Pura Lingsar tidak hanya menjadi pusat keagamaan, tetapi juga menjadi simbol harmoni dan toleransi antaragama. Keberadaannya yang unik menggarisbawahi nilai-nilai toleransi, persaudaraan, dan kerukunan yang merupakan bagian penting dari identitas budaya Lombok.</p>
                
                <p>Pura Lingsar tidak hanya menarik bagi wisatawan karena keindahan arsitektur dan ritual keagamaannya, tetapi juga memberikan pengalaman yang mendalam tentang pluralisme agama dan harmoni sosial yang merupakan warisan kuno yang terpelihara dengan baik di Pulau Lombok.</p>

                <p>Lokasi dan Luas: Taman Nasional Way Kambas terletak di Kabupaten Lampung Timur dan Kabupaten Tanggamus, Provinsi Lampung, Sumatera, Indonesia. Taman nasional ini memiliki luas sekitar 1.300 kilometer persegi dan dikenal sebagai salah satu kawasan konservasi terbesar di Indonesia.</p>
        
                <p>Ekosistem: Taman Nasional Way Kambas terdiri dari berbagai tipe habitat, termasuk hutan hujan dataran rendah, rawa-rawa, sungai, dan padang rumput. Ekosistem yang beragam ini menjadi rumah bagi berbagai spesies flora dan fauna yang langka dan dilindungi, termasuk gajah Sumatera, harimau Sumatera, badak Sumatera, orangutan Sumatera, serta berbagai jenis burung dan reptil. </p>
                
                <p>Konservasi Gajah: Salah satu tujuan utama Taman Nasional Way Kambas adalah untuk melindungi dan melestarikan populasi gajah Sumatera yang terancam punah. Di dalam taman nasional ini, terdapat Pusat Latihan Gajah (PLG) yang menjadi pusat rehabilitasi dan pelatihan bagi gajah-gajah yang telah diselamatkan dari berbagai ancaman, seperti perburuan ilegal dan konflik dengan manusia.</p>

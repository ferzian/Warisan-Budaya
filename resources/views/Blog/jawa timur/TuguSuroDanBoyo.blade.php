                <p>Lokasi: Tugu Surabaya terletak di persimpangan antara Jalan Pahlawan dan Jalan Tugu, yang merupakan daerah pusat kota Surabaya. Lokasinya yang strategis membuat tugu ini menjadi titik penting dan mudah diakses oleh para pengunjung.</p>
                

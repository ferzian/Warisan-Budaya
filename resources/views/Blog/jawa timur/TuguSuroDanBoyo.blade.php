                <p>Arsitektur: Tugu Surabaya memiliki desain yang unik dan mencolok. Terdiri dari tiga pilar utama yang melambangkan semangat perjuangan, tiap pilar memiliki bentuk yang berbeda. Pilar-pilar tersebut melambangkan tiga unsur utama yang terlibat dalam perjuangan, yaitu tentara, rakyat, dan pemuda. Tinggi tugu ini mencapai sekitar 41 meter, menjadikannya salah satu landmark tertinggi di Surabaya.</p>
